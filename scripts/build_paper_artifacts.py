# Figure — Cleaner version of the ablation plot
# ---------------------------------------------------------------------------
def rebuild_ablation_figure() -> None:
    df = pd.read_csv(RESULTS / "ablation_fipca_rank.csv")
    ranks = df["rank"].tolist()
    ssims = df["final_ssim"].tolist()
    bytes_pr = df["bytes_per_round"].tolist()

    fig, ax1 = plt.subplots(figsize=(8.5, 5))
    color_q = "#2ca02c"
    color_c = "#1f77b4"

    ax1.set_xlabel("FIPCA projection rank  $d$", fontsize=11)
    ax1.set_ylabel("Final-round eval SSIM", color=color_q, fontsize=11)
    ax1.plot(ranks, ssims, "o-", color=color_q, linewidth=2.5, markersize=11)
    ax1.set_ylim(0.0, 1.0)  # honest scale relative to the maximum possible SSIM
    ax1.set_xticks(ranks)
    ax1.tick_params(axis="y", labelcolor=color_q)
    ax1.grid(True, alpha=0.3)
    # Annotate the SSIM values
    for x, y in zip(ranks, ssims):
        ax1.annotate(f"{y:.3f}", (x, y), xytext=(8, 6), textcoords="offset points",
                     fontsize=9, color=color_q)
    # Reference: IFCNN final SSIM as the "non-private upper bound"
    ax1.axhline(0.972, color=color_q, linestyle=":", alpha=0.5)
    ax1.text(ranks[0], 0.98, "IFCNN+FedAvg (non-private upper bound, SSIM=0.972)",
             color=color_q, fontsize=9)

    ax2 = ax1.twinx()
    ax2.set_ylabel("Upstream bytes per round (log)", color=color_c, fontsize=11)
    ax2.plot(ranks, bytes_pr, "s--", color=color_c, linewidth=2.5, markersize=11)
    ax2.set_yscale("log")
    ax2.set_ylim(50, 1e8)
    ax2.tick_params(axis="y", labelcolor=color_c)
    for x, y in zip(ranks, bytes_pr):
        ax2.annotate(f"{int(y)} B", (x, y), xytext=(8, -14), textcoords="offset points",
                     fontsize=9, color=color_c)
    ax2.axhline(59_178_012, color="#ff7f0e", linestyle=":", alpha=0.6)
    ax2.text(ranks[-1], 59_178_012 * 1.4, "DP-SGD baseline (59 MB)", color="#ff7f0e",
             fontsize=9, ha="right")

    plt.title("FIPCA rank ablation: quality plateau vs upstream bandwidth", fontsize=12)
    fig.tight_layout()
    out = FIGURES / "ablation_fipca_rank_v2.png"
    plt.savefig(out, dpi=160, bbox_inches="tight")
    plt.close(fig)
    print(f"Rebuilt {out}")



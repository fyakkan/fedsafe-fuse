def fmt_bytes(n: float) -> str:
    """Compact human-readable byte size for table cells (LaTeX-safe)."""
    n = float(n)
    if n >= 1e6:
        return f"{n / 1e6:.2f}\\,MB"
    if n >= 1e3:
        return f"{n / 1e3:.1f}\\,KB"
    return f"{int(n)}\\,B"


# ---------------------------------------------------------------------------
# Table 1 — Main federated training results (Phase 3)
# Method × Privacy × Final SSIM × Final PSNR × bytes/round
# ---------------------------------------------------------------------------
def build_main_results_table() -> str:
    df = pd.read_csv(RESULTS / "round1_main.csv")
    rows = []
    for tag, label, privacy in [
        ("ifcnn_fedavg", "FedAvg + IFCNN (B2)", "none"),
        ("fedsafe_dpsgd", "FedAvg + DP-SGD (B1)", "DP, $\\sigma\\!=\\!0.5$"),
        ("fedsafe_fipca", "FedSafe-Fuse + FIPCA (\\textbf{ours})", "rank-32 proj."),
    ]:
        sub = df[df["tag"] == tag]
        final = sub.dropna(subset=["eval_ssim"]).iloc[-1]
        bytes_pr = float(sub["bytes_per_round"].iloc[0])
        rows.append({
            "method": label,
            "privacy": privacy,
            "ssim": float(final["eval_ssim"]),
            "psnr": float(final["eval_psnr"]),
            "bytes": bytes_pr,
        })

    lines = [
        "% Auto-generated by scripts/build_paper_artifacts.py",
        "\\begin{table*}[t]",
        "\\centering",
        "\\caption{Federated training results on MedMNIST OrganAMNIST "
        "(K=3 clients, T=50 rounds, E=5 local epochs, batch=16, "
        "40 samples/client/local-epoch; see Methods).}",
        "\\label{tab:main_results}",
        "\\renewcommand{\\arraystretch}{1.15}",
        "\\begin{tabular}{lcccr}",
        "\\toprule",
        "Method & Privacy mechanism & SSIM~$\\uparrow$ & PSNR (dB)~$\\uparrow$ & Bytes per round~$\\downarrow$ \\\\",
        "\\midrule",
    ]
    for r in rows:
        lines.append(
            f"{r['method']} & {r['privacy']} & "
            f"{r['ssim']:.3f} & {r['psnr']:.2f} & {fmt_bytes(r['bytes'])} \\\\"
        )
    lines += ["\\bottomrule", "\\end{tabular}", "\\end{table*}", ""]
    return "\n".join(lines)


